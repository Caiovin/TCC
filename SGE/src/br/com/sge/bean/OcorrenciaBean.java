package br.com.sge.bean;

public class OcorrenciaBean {
    private int codOcorrencia;
    private int rmAluno;
    private String nmAluno;
    private String descOcorrencia;
    private String dtOcorrencia;
    
    
	/**
	 * @return the nmAluno
	 */
	public String getNmAluno() {
		return nmAluno;
	}
	/**
	 * @param nmAluno the nmAluno to set
	 */
	public void setNmAluno(String nmAluno) {
		this.nmAluno = nmAluno;
	}
	/**
	 * @return the dtOcorrencia
	 */
	public String getDtOcorrencia() {
		return dtOcorrencia;
	}
	/**
	 * @param dtOcorrencia the dtOcorrencia to set
	 */
	public void setDtOcorrencia(String dtOcorrencia) {
		this.dtOcorrencia = dtOcorrencia;
	}
	/**
	 * @return the codOcorrencia
	 */
	public int getCodOcorrencia() {
		return codOcorrencia;
	}
	/**
	 * @param codOcorrencia the codOcorrencia to set
	 */
	public void setCodOcorrencia(int codOcorrencia) {
		this.codOcorrencia = codOcorrencia;
	}
	/**
	 * @return the rmAluno
	 */
	public int getRmAluno() {
		return rmAluno;
	}
	/**
	 * @param rmAluno the rmAluno to set
	 */
	public void setRmAluno(int rmAluno) {
		this.rmAluno = rmAluno;
	}
	/**
	 * @return the descOcorrencia
	 */
	public String getDescOcorrencia() {
		return descOcorrencia;
	}
	/**
	 * @param descOcorrencia the descOcorrencia to set
	 */
	public void setDescOcorrencia(String descOcorrencia) {
		this.descOcorrencia = descOcorrencia;
	}

    
}