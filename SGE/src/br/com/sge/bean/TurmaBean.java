package br.com.sge.bean;

public class TurmaBean {
    private int codTurma;
    private String nmTurma;
    private String prdTurma;
    
    
	/**
	 * @return the codTurma
	 */
	public int getCodTurma() {
		return codTurma;
	}
	/**
	 * @param codTurma the codTurma to set
	 */
	public void setCodTurma(int codTurma) {
		this.codTurma = codTurma;
	}
	/**
	 * @return the nmTurma
	 */
	public String getNmTurma() {
		return nmTurma;
	}
	/**
	 * @param nmTurma the nmTurma to set
	 */
	public void setNmTurma(String nmTurma) {
		this.nmTurma = nmTurma;
	}
	/**
	 * @return the prdTurma
	 */
	public String getPrdTurma() {
		return prdTurma;
	}
	/**
	 * @param prdTurma the prdTurma to set
	 */
	public void setPrdTurma(String prdTurma) {
		this.prdTurma = prdTurma;
	}

    
}