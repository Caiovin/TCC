package br.com.sge.bean;

public class AtividadeBean {
	private int codAtividade;
	private int codTurma;
	private int codProfessor;
	private String descAtividade;
	private String tipoAtividade;
	private String dtAtividade;
	private String dtEntrega;
	private float valorAtividade;
	
	
	/**
	 * @return the codAtividade
	 */
	public int getCodAtividade() {
		return codAtividade;
	}
	/**
	 * @param codAtividade the codAtividade to set
	 */
	public void setCodAtividade(int codAtividade) {
		this.codAtividade = codAtividade;
	}
	/**
	 * @return the codTurma
	 */
	public int getCodTurma() {
		return codTurma;
	}
	/**
	 * @param codTurma the codTurma to set
	 */
	public void setCodTurma(int codTurma) {
		this.codTurma = codTurma;
	}
	/**
	 * @return the codProfessor
	 */
	public int getCodProfessor() {
		return codProfessor;
	}
	/**
	 * @param codProfessor the codProfessor to set
	 */
	public void setCodProfessor(int codProfessor) {
		this.codProfessor = codProfessor;
	}
	/**
	 * @return the descAtividade
	 */
	public String getDescAtividade() {
		return descAtividade;
	}
	/**
	 * @param descAtividade the descAtividade to set
	 */
	public void setDescAtividade(String descAtividade) {
		this.descAtividade = descAtividade;
	}
	/**
	 * @return the tipoAtividade
	 */
	public String getTipoAtividade() {
		return tipoAtividade;
	}
	/**
	 * @param tipoAtividade the tipoAtividade to set
	 */
	public void setTipoAtividade(String tipoAtividade) {
		this.tipoAtividade = tipoAtividade;
	}
	/**
	 * @return the dtAtividade
	 */
	public String getDtAtividade() {
		return dtAtividade;
	}
	/**
	 * @param dtAtividade the dtAtividade to set
	 */
	public void setDtAtividade(String dtAtividade) {
		this.dtAtividade = dtAtividade;
	}
	/**
	 * @return the dtEntrega
	 */
	public String getDtEntrega() {
		return dtEntrega;
	}
	/**
	 * @param dtEntrega the dtEntrega to set
	 */
	public void setDtEntrega(String dtEntrega) {
		this.dtEntrega = dtEntrega;
	}
	/**
	 * @return the valorAtividade
	 */
	public float getValorAtividade() {
		return valorAtividade;
	}
	/**
	 * @param valorAtividade the valorAtividade to set
	 */
	public void setValorAtividade(float valorAtividade) {
		this.valorAtividade = valorAtividade;
	}
	
	
}