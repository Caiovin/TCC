public class ResponsavelBean {
    private int codResponsavel;
    private String nmResponsavel;
    private String telResponsavel;
    private String celResponsavel;
    private String cep;
    private int numEndereco;
    private String complemento;
    private String emailResponsavel;
    
    
	/**
	 * @return the codResponsavel
	 */
	public int getCodResponsavel() {
		return codResponsavel;
	}
	/**
	 * @param codResponsavel the codResponsavel to set
	 */
	public void setCodResponsavel(int codResponsavel) {
		this.codResponsavel = codResponsavel;
	}
	/**
	 * @return the nmResponsavel
	 */
	public String getNmResponsavel() {
		return nmResponsavel;
	}
	/**
	 * @param nmResponsavel the nmResponsavel to set
	 */
	public void setNmResponsavel(String nmResponsavel) {
		this.nmResponsavel = nmResponsavel;
	}
	/**
	 * @return the telResponsavel
	 */
	public String getTelResponsavel() {
		return telResponsavel;
	}
	/**
	 * @param telResponsavel the telResponsavel to set
	 */
	public void setTelResponsavel(String telResponsavel) {
		this.telResponsavel = telResponsavel;
	}
	/**
	 * @return the celResponsavel
	 */
	public String getCelResponsavel() {
		return celResponsavel;
	}
	/**
	 * @param celResponsavel the celResponsavel to set
	 */
	public void setCelResponsavel(String celResponsavel) {
		this.celResponsavel = celResponsavel;
	}
	/**
	 * @return the cep
	 */
	public String getCep() {
		return cep;
	}
	/**
	 * @param cep the cep to set
	 */
	public void setCep(String cep) {
		this.cep = cep;
	}
	/**
	 * @return the numEndereco
	 */
	public int getNumEndereco() {
		return numEndereco;
	}
	/**
	 * @param NumEndereco the numEndereco to set
	 */
	public void setNumEndereco(int NumEndereco) {
		this.numEndereco = NumEndereco;
	}
	/**
	 * @return the complemento
	 */
	public String getComplemento() {
		return complemento;
	}
	/**
	 * @param complemento the complemento to set
	 */
	public void setComplemento(String complemento) {
		this.complemento = complemento;
	}
	/**
	 * @return the emailResponsavel
	 */
	public String getEmailResponsavel() {
		return emailResponsavel;
	}
	/**
	 * @param emailResponsavel the emailResponsavel to set
	 */
	public void setEmailResponsavel(String emailResponsavel) {
		this.emailResponsavel = emailResponsavel;
	}

    
}